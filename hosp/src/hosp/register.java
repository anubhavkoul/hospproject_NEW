    private void insertData(RegisterationData data) {
        Connection connection = null;
        Statement statement = null; 
        try {           
            connection = DatabaseHelper.getConnection();
            statement = connection.createStatement();
            
            String query = "Insert into register_table values("
                    +data.getRegistrationId()+", '"
                    +data.getName()+"', "
                    +data.getDateOfBirth()+", '"
                    +data.getMonthOfBirth()+"', "
                    +data.getYearOfBirth()+", '"
                    +data.getGender()+"', '"
                    +data.getAddress()+"', '"
                    +data.getCity()+"', '"
                    +data.getState()+"', '"
                    +data.getTelephoneNumber()+"', '"
                    +data.getMobileNumber()+"', '"
                    +data.getEmail()+"', '"
                    +data.getWardNumber()+"', '"
                    +data.getBloodGroup()+"', '"
                    +data.getPreviousAilments()+"', "
                    +data.getDateOfAdmit()+", '"
                    +data.getMonthOfAdmit()+"', "
                    +data.getYearOfAdmit()+")";
            statement.executeUpdate(query);
             
        } catch (SQLException e) {
            e.printStackTrace();
        } finally {
            if (connection != null) {
                try {
                    connection.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                    System.out.println(e);
                }
            }
        }
    }

